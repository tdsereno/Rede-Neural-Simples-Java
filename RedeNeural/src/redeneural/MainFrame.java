    private void jBtTestarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtTestarActionPerformed
        String dadosTeste = jTFDadosTestarRede.getText();
        String[] dados = dadosTeste.split(",");
        final Color[] cores = new Color[neuroniosSaida];
        if (dados.length != neuroniosEntrada) {
            JOptionPane.showMessageDialog(rootPane, "Dados para teste invalidos");

        } else {
            Double[] dataTest = new Double[neuroniosEntrada];
            for (int i = 0; i < neuroniosEntrada; i++) {
                dataTest[i] = Double.parseDouble(dados[i]);
            }

            rede.test(dataTest); //testar rede

            Double[] saida = rede.getOutput();

            DefaultTableModel tabelaModelo = (DefaultTableModel) jTTestar.getModel();
            tabelaModelo.setNumRows(0); //limpa a tabela
            for (int i = 0; i < saida.length; i++) {//seta cor
                tabelaModelo.addRow(new String[]{"Neuronio " + (i + 1), "" + formater.format(saida[i])});
                if (saida[i] > 0.5) {
                    cores[i] = Color.LIGHT_GRAY;
                }
                if (saida[i] > 0.7) {
                    cores[i] = Color.YELLOW;
                }
                if (saida[i] > 0.9) {
                    cores[i] = Color.RED;
                }
            }
        }
        jTTestar.setDefaultRenderer(Object.class, new DefaultTableCellRenderer() {
            @Override
            public Component getTableCellRendererComponent(JTable table, Object value, boolean isSelected, boolean hasFocus, int row, int column) {
                final Component c = super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, column);
                c.setBackground(cores[row]);
                return c;
            }
        });
    }//GEN-LAST:event_jBtTestarActionPerformed

