    public void definirNumeroNeuronios() {

        try {

            neuroniosEntrada = Integer.parseInt(jTFNEntrada.getText());
            neuroniosIntermediaria = Integer.parseInt(jTFNIntermediaria.getText());
            neuroniosSaida = Integer.parseInt(jTFNeuroniosSaida.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Numero de neuronios invalidos");
        }

    }

